#include "libft.h"

char	*ft_strncat(char *restrict s1, const char *restrict s2, size_t n)
{
	char *dst;
	char *src;

	dst = s1;
	src = (char *)s2;
	while (*dst != '\0')
		dst++;
	while (*src != '\0' && n > 0)
	{
		*dst = *src;
		dst++;
		src++;
		n--;
	}
	*dst = '\0';
	return (s1);
}
